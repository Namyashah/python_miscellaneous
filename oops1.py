class emp:
    def setdata(self,y,z,a):
        self.name = y
        self.age = z
        self.salary = a
    def display(x):
        print(x.name)
        print(x.age)
        print(x.salary)
e1 = emp()
e2 = emp()
e1.setdata("aaa","23","65000")
e1.display()
print()
e2.setdata("bbb","45","98000")
e2.display()
